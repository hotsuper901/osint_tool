def scan_port(host, port, timeout=1.5):
    s = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    s.settimeout(timeout)
    result = s.connect_ex((host, port))
    s.close()
    if result == 0:
        service = COMMON_PORTS.get(port, "Unknown")
        return (port, service)
    return None

def run(target):
    print(f"\n{'='*60}")
    print(f"  PORT SCANNER - Target: {target}")
    print(f"{'='*60}")

    import re
    host = target.strip()
    port_range = None
    if ':' in target:
        parts = target.split(':')
        host = parts[0]
        try:
            port_range = tuple(map(int, parts[1].split('-')))
        except:
            pass

    try:
        ip = socket.gethostbyname(host)
        print(f"\n  [i] Resolved: {host} -> {ip}")
    except:
        print(f"\n  [!] Could not resolve {host}")
        return

    output = [f"Target: {host}", f"IP: {ip}"]
    ports_to_scan = ALL_PORTS

    print(f"\n  [i] Scanning {len(ports_to_scan)} ports...")
    print("  [i] This may take a moment\n")

    found = []
    with concurrent.futures.ThreadPoolExecutor(max_workers=50) as executor:
        future_map = {executor.submit(scan_port, ip, p): p for p in ports_to_scan}
        done = 0
        total = len(ports_to_scan)
        for future in concurrent.futures.as_completed(future_map):
            done += 1
            result = future.result()
            if result:
                found.append(result)
                print(f"  [+] OPEN: {result[0]:5d}  {result[1]}")

    if not found:
        print("\n  [-] No open ports found")
        output.append("No open ports found")
    else:
        output.append(f"\nOpen ports ({len(found)}):")
        for port, service in sorted(found):
            output.append(f"  {port:5d}  {service}")
            print(f"  {port:5d}  {service}")

    save_result("portscan", host, "\n".join(output))
